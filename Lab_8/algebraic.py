import math

EPS = 1e-5


# ============================================================
# Завдання 6-7: Робота з коефіцієнтами через текстовий файл
# Формат: a_n a_{n-1} ... a_1 a_0  (від старшого до молодшого)
# ============================================================
def write_coeffs(filename, coeffs):
    with open(filename, 'w', encoding='utf-8') as f:
        f.write(' '.join(map(str, coeffs)))


def read_coeffs(filename):
    with open(filename, 'r', encoding='utf-8') as f:
        return [float(x) for x in f.read().split()]


# ============================================================
# Схема Горнера для P(x) = a_0*x^n + a_1*x^{n-1} + ... + a_n
#
# Розклад: P(x) = (x - x_n) * Q(x) + b_n,
# де b_n = P(x_n), а коефіцієнти Q(x) = b_0*x^{n-1} + ... + b_{n-1}
# обчислюються рекурентно:
#   b_0 = a_0
#   b_i = b_{i-1} * x_n + a_i,   i = 1, ..., n
# ============================================================
def horner(coeffs, x):
    """
    Обчислює P(x) за схемою Горнера.
    Повертає (P(x), [b_0, b_1, ..., b_n]) -- значення і всі b_i.
    """
    b = [coeffs[0]]
    for i in range(1, len(coeffs)):
        b.append(b[-1] * x + coeffs[i])
    return b[-1], b


# ============================================================
# Завдання 7 (друга частина):
# Функція, що повертає значення многочлена в довільній точці
# ============================================================
def poly_value(coeffs, x):
    """Значення многочлена P(x) у точці x"""
    return horner(coeffs, x)[0]


# ============================================================
# Завдання 8: Метод Ньютона зі схемою Горнера
# x_{n+1} = x_n - P(x_n) / P'(x_n)
#
# Хитрість: P(x_n) рахуємо схемою Горнера за O(n).
# P'(x_n) = повторне застосування Горнера до b_0..b_{n-1}
# (математичне обґрунтування -- у методичці на стор. 10-11).
# ============================================================
def newton_horner(coeffs, x0, eps=EPS, max_iter=1000):
    """Метод Ньютона з обчисленням P і P' через схему Горнера"""
    x_old = x0
    for k in range(1, max_iter + 1):
        # Перший прохід Горнера: P(x_old) і коефіцієнти b
        P_val, b = horner(coeffs, x_old)
        # Другий прохід по b[:-1]: P'(x_old)
        dP_val, _ = horner(b[:-1], x_old)
        if dP_val == 0:
            raise ZeroDivisionError("P'(x) = 0")
        x_new = x_old - P_val / dP_val
        # Критерій зупинки
        if abs(x_new - x_old) < eps and abs(P_val) < eps:
            return x_new, k
        x_old = x_new
    return x_old, max_iter


# ============================================================
# Завдання 9: Метод Ліна для комплексних коренів
#
# Ідея: комплексні корені йдуть парами (комплексно-спряжені),
# отже шукаємо квадратний дільник P(x):
#   P(x) = (x^2 + p*x + q) * Q(x) + (r1*x + r0)
# Коефіцієнти діляться "схемою Горнера для квадратного дільника":
#   b_0 = a_0
#   b_1 = a_1 - p*b_0
#   b_i = a_i - p*b_{i-1} - q*b_{i-2}   (i = 2, ..., n)
# Остача: r1 = b_{n-1},  r0 = b_n + p*b_{n-1}
#
# Шукаємо (p, q) такі, щоб r0 = r1 = 0. Це система 2 нелінійних
# рівнянь -- розв'язуємо методом Ньютона. Часткові похідні
# рахуються через ще одну подібну рекурентність по c_i:
#   c_0 = b_0,  c_1 = b_1 - p*c_0
#   c_i = b_i - p*c_{i-1} - q*c_{i-2}
#
# З цих коефіцієнтів складається якобіан системи.
# ============================================================
def lin_method(coeffs, p0=0.0, q0=2.0, eps=EPS, max_iter=1000):
    """
    Метод Ліна: знаходить квадратний дільник x^2 + p*x + q
    многочлена P(x). Повертає (p, q, ітерації, два корені).
    """
    a = list(coeffs)
    n = len(a) - 1  # степінь
    p, q = p0, q0

    for k in range(1, max_iter + 1):
        # 1) Коефіцієнти b_i (ділення P на x^2 + p*x + q)
        b = [0.0] * (n + 1)
        b[0] = a[0]
        b[1] = a[1] - p * b[0]
        for i in range(2, n + 1):
            b[i] = a[i] - p * b[i - 1] - q * b[i - 2]

        # 2) Коефіцієнти c_i (для якобіана)
        c = [0.0] * (n + 1)
        c[0] = b[0]
        c[1] = b[1] - p * c[0]
        for i in range(2, n + 1):
            c[i] = b[i] - p * c[i - 1] - q * c[i - 2]

        # 3) Лінійна остача r1*x + r0
        r1 = b[n - 1]
        r0 = b[n] + p * b[n - 1]

        # 4) Якобіан і поправки методом Ньютона для системи r1=0, r0=0
        J11 = c[n - 2]
        J12 = c[n - 3] if n >= 3 else 0.0
        J21 = c[n - 1] - b[n - 1]
        J22 = c[n - 2]

        det = J11 * J22 - J12 * J21
        if abs(det) < 1e-30:
            break
        dp = (r1 * J22 - r0 * J12) / det
        dq = (r0 * J11 - r1 * J21) / det

        p += dp
        q += dq

        if abs(dp) < eps and abs(dq) < eps:
            break

    # Корені квадратного тричлена x^2 + p*x + q = 0
    D = p * p - 4 * q
    if D < 0:
        re = -p / 2
        im = math.sqrt(-D) / 2
        roots = (complex(re, im), complex(re, -im))
    else:
        sq = math.sqrt(D)
        roots = ((-p + sq) / 2, (-p - sq) / 2)

    return p, q, k, roots


