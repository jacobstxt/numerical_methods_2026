# ОСНОВНА ПРОГРАМА
# ============================================================
def main():
    print("=" * 72)
    print("P(x) = x^3 - 3x^2 + 4x - 2 = 0")
    print("=" * 72)

    # --- Завдання 6: записуємо коефіцієнти у файл ---
    coeffs_orig = [1.0, -3.0, 4.0, -2.0]
    write_coeffs("poly_coeffs.txt", coeffs_orig)
    print("\n[Завдання 6] Коефіцієнти {1, -3, 4, -2} записано у poly_coeffs.txt")

    # --- Завдання 7: зчитуємо ---
    coeffs = read_coeffs("poly_coeffs.txt")
    print(f"[Завдання 7] Зчитано: {coeffs}")

    # Перевірка обчислення P(x) у кількох точках
    print("\n  Перевірка обчислення P(x) за схемою Горнера:")
    for x_test in [0.0, 1.0, 2.0, -1.0]:
        val = poly_value(coeffs, x_test)
        # Перевірка прямим обчисленням
        direct = (coeffs[0] * x_test ** 3 + coeffs[1] * x_test ** 2
                  + coeffs[2] * x_test + coeffs[3])
        print(f"    P({x_test:+.1f}) = {val:+.4f}  "
              f"(пряме обчислення: {direct:+.4f})")

    # --- Завдання 8: Ньютон + Горнер для дійсного кореня ---
    x0 = 2.0  # початкове наближення
    real_root, k = newton_horner(coeffs, x0)
    print(f"\n[Завдання 8] Метод Ньютона зі схемою Горнера:")
    print(f"    Початкове наближення x0 = {x0}")
    print(f"    Дійсний корінь:  x = {real_root:.8f}")
    print(f"    P(x) = {poly_value(coeffs, real_root):.2e}")
    print(f"    Ітерацій: {k}")

    # --- Завдання 9: метод Ліна для комплексних коренів ---
    # Початкове наближення (p0, q0) обираємо, щоб x^2+p*x+q
    # було далеким від лінійних множників, що містять дійсний корінь.
    p0, q0 = 0.0, 2.0
    p, q, k, complex_roots = lin_method(coeffs, p0=p0, q0=q0)
    print(f"\n[Завдання 9] Метод Ліна для комплексних коренів:")
    print(f"    Початкові наближення: p0 = {p0}, q0 = {q0}")
    print(f"    Знайдений квадратний дільник: x^2 + ({p:.6f})x + ({q:.6f})")
    print(f"    Ітерацій: {k}")
    print(f"    Комплексні корені:")
    for r in complex_roots:
        print(f"      x = {r}")


if __name__ == "__main__":
    main()